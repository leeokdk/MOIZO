package com.moim.vo;

import org.apache.commons.lang3.builder.ToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;
import org.springframework.format.annotation.NumberFormat;
import org.springframework.format.annotation.NumberFormat.Style;

public class MoimSearchVO extends PagingVO {
	
	private String searchWord;
	private String searchType;
	private String searchCategory;
	
	private String searchHobby;
	private String searchLocationBig;
	private String searchLocationMid;
	private String searchGender;
	@NumberFormat(style = Style.NUMBER)
	private String searchAge;
	
	@Override
	public String toString() {
		return ToStringBuilder.reflectionToString(this, ToStringStyle.MULTI_LINE_STYLE);
	}
	
	
	
	public String getSearchHobby() {
		return searchHobby;
	}



	public void setSearchHobby(String searchHobby) {
		this.searchHobby = searchHobby;
	}



	public String getSearchLocationBig() {
		return searchLocationBig;
	}



	public void setSearchLocationBig(String searchLocationBig) {
		this.searchLocationBig = searchLocationBig;
	}



	public String getSearchLocationMid() {
		return searchLocationMid;
	}



	public void setSearchLocationMid(String searchLocationMid) {
		this.searchLocationMid = searchLocationMid;
	}



	public String getSearchGender() {
		return searchGender;
	}



	public void setSearchGender(String searchGender) {
		this.searchGender = searchGender;
	}



	public String getSearchAge() {
		return searchAge;
	}



	public void setSearchAge(String searchAge) {
		this.searchAge = searchAge;
	}

	public String getSearchWord() {
		return searchWord;
	}
	public void setSearchWord(String searchWord) {
		this.searchWord = searchWord;
	}
	public String getSearchType() {
		return searchType;
	}
	public void setSearchType(String searchType) {
		this.searchType = searchType;
	}


	public String getSearchCategory() {
		return searchCategory;
	}


	public void setSearchCategory(String searchCategory) {
		this.searchCategory = searchCategory;
	}
}
