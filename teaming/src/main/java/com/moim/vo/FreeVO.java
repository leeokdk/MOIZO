package com.moim.vo;

import org.apache.commons.lang3.builder.ToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

public class FreeVO {
	private String moimCode;                /* 모임코드*/
	private int freeNo;                  /* 게시글 넘버*/
	private String freeTitle;               /* 게시글 제목*/
	private String freeWriter;              /* 게시글 작성자*/
	private String freeContent;             /* 게시글 내용*/
	private int freeHit;                 /* 게시글 조회수*/
	private String freeRegDate;             /* 게시글 등록날짜*/
	private String freeModDate;             /* 게시글 수정날짜*/
	private String freeDelYn;               /* 게시글 삭제여부*/
	private String rnum;
	private String userName;
	@Override
	public String toString() {
		return ToStringBuilder.reflectionToString(this, ToStringStyle.MULTI_LINE_STYLE);
	}

	public String getMoimCode() {
		return moimCode;
	}

	public void setMoimCode(String moimCode) {
		this.moimCode = moimCode;
	}

	public int getFreeNo() {
		return freeNo;
	}

	public void setFreeNo(int freeNo) {
		this.freeNo = freeNo;
	}

	public String getFreeTitle() {
		return freeTitle;
	}

	public void setFreeTitle(String freeTitle) {
		this.freeTitle = freeTitle;
	}

	public String getFreeWriter() {
		return freeWriter;
	}

	public void setFreeWriter(String freeWriter) {
		this.freeWriter = freeWriter;
	}

	public String getFreeContent() {
		return freeContent;
	}

	public void setFreeContent(String freeContent) {
		this.freeContent = freeContent;
	}

	public int getFreeHit() {
		return freeHit;
	}

	public void setFreeHit(int freeHit) {
		this.freeHit = freeHit;
	}

	public String getFreeRegDate() {
		return freeRegDate;
	}

	public void setFreeRegDate(String freeRegDate) {
		this.freeRegDate = freeRegDate;
	}

	public String getFreeModDate() {
		return freeModDate;
	}

	public void setFreeModDate(String freeModDate) {
		this.freeModDate = freeModDate;
	}

	public String getFreeDelYn() {
		return freeDelYn;
	}

	public void setFreeDelYn(String freeDelYn) {
		this.freeDelYn = freeDelYn;
	}

	public String getRnum() {
		return rnum;
	}

	public void setRnum(String rnum) {
		this.rnum = rnum;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}



}
