package com.moim.vo;

import java.util.List;

import org.apache.commons.lang3.builder.ToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

/**
 * 
 * 모임 일정을 위한 게시판 생성
 * 
 * @author DNA
 * @since 2021.07.28
 *
 */
public class MoimScheduleVO {
	
	private String moimCode;            /* 모임코드 */
	private int mScheduleNo;            /* 게시글 넘버 */
	private String mScheduleTitle;      /* 게시글 제목 */
	private String mScheduleContent;    /* 게시글 내용 */
	private int mScheduleEnter;         /* 스케줄 참여인원 */
	private String mScheduleRegDate;    /* 게시글 등록날짜 */
	private String mScheduleModDate;    /* 스케줄 날짜 */
	private String mScheduleStartTime;  /* 스케줄 시작시간 */
	private String mScheduleEndTime;    /* 스케줄 종료시간 */
	private String mSchedulePlace;      /* 스케줄 장소 */
	private int mScheduleEnterMax;      /* 스케줄 최대 참여인원 */
	
	private String memberId;            /* 멤버아이디 */
	
	private String moimName;            /* 모임이름 */
	
	
	// 스케줄에 참여중인 회원아이디 
	private List<String> memberList;
	
	@Override
	public String toString() {
		return ToStringBuilder.reflectionToString(this, ToStringStyle.MULTI_LINE_STYLE);
	}

	public String getMoimName() {
		return moimName;
	}

	public void setMoimName(String moimName) {
		this.moimName = moimName;
	}

	public String getMoimCode() {
		return moimCode;
	}

	public void setMoimCode(String moimCode) {
		this.moimCode = moimCode;
	}

	public int getmScheduleNo() {
		return mScheduleNo;
	}

	public void setmScheduleNo(int mScheduleNo) {
		this.mScheduleNo = mScheduleNo;
	}

	public String getmScheduleTitle() {
		return mScheduleTitle;
	}

	public void setmScheduleTitle(String mScheduleTitle) {
		this.mScheduleTitle = mScheduleTitle;
	}

	public String getmScheduleContent() {
		return mScheduleContent;
	}

	public void setmScheduleContent(String mScheduleContent) {
		this.mScheduleContent = mScheduleContent;
	}

	public int getmScheduleEnter() {
		return mScheduleEnter;
	}

	public void setmScheduleEnter(int mScheduleEnter) {
		this.mScheduleEnter = mScheduleEnter;
	}

	public String getmScheduleRegDate() {
		return mScheduleRegDate;
	}

	public void setmScheduleRegDate(String mScheduleRegDate) {
		this.mScheduleRegDate = mScheduleRegDate;
	}

	public String getmScheduleModDate() {
		return mScheduleModDate;
	}

	public void setmScheduleModDate(String mScheduleModDate) {
		this.mScheduleModDate = mScheduleModDate;
	}

	public String getmScheduleStartTime() {
		return mScheduleStartTime;
	}

	public void setmScheduleStartTime(String mScheduleStartTime) {
		this.mScheduleStartTime = mScheduleStartTime;
	}

	public String getmScheduleEndTime() {
		return mScheduleEndTime;
	}

	public void setmScheduleEndTime(String mScheduleEndTime) {
		this.mScheduleEndTime = mScheduleEndTime;
	}

	public String getmSchedulePlace() {
		return mSchedulePlace;
	}

	public void setmSchedulePlace(String mSchedulePlace) {
		this.mSchedulePlace = mSchedulePlace;
	}

	public int getmScheduleEnterMax() {
		return mScheduleEnterMax;
	}

	public void setmScheduleEnterMax(int mScheduleEnterMax) {
		this.mScheduleEnterMax = mScheduleEnterMax;
	}

	public String getMemberId() {
		return memberId;
	}

	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}

	public List<String> getMemberList() {
		return memberList;
	}

	public void setMemberList(List<String> memberList) {
		this.memberList = memberList;
	}

	
	
}
